export default {
  methods: {
    refreshData(url, params) {
      if(params.totalApCnt !== undefined){
        this.totalApCnt = params.totalApCnt
      }
      let $this = this;

      this.$axios.get(url, {params:params}).then(res => {
        /** data 에 선언된 값은 고정! 쿼리에서 조회할 때 맞춰줘야 함 */

         for (const type of Object.keys($this.$data)) {
             let info = res;
             if(type == 'totalApCnt'){
               if (params.totalApCnt !== undefined) { //위젯 개별 설정 저장 값이 있을 경우 해당 값 적용
                 $this[type] = params.totalApCnt;
               } else {
                 $this[type] = info[type];
               }
             }else{ //현재 ap만 애니메이션 적용 0~N
                $this[type] = 0;
             }

             const gsapParams = {
               data: this.$data,
               type: type,
               cnt: info[type]
             };
             if(type == 'totalApCnt'){
               if(params.totalApCnt !== undefined){ //위젯 개별 설정 저장 값이 있을 경우 해당 값 적용
                 gsapParams.cnt = params.totalApCnt;
               }
             }
             this.$store.dispatch('gsap/applyAnimation', gsapParams);
         }
      })
    },
  },

  filters: {
    toFixed(value) { return value ? value.toFixed(0) : 0 }
  },

  data() {
    return {
      curApCnt: 0,
      totalApCnt: 0,
      curConnCnt: 0
    }
  }
}
